    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
    String adminID = txtAdminID.getText(); // Get AdminID from input field
    String password = new String(txtPassword.getPassword()); // Get password from password field

    if (adminID.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Admin ID and Password cannot be empty!");
        return;
    }

    String query = "SELECT * FROM admin WHERE adminid = ? AND password = ?";

    try (Connection conn = DBconnection.connect();
         PreparedStatement stmt = conn.prepareStatement(query)) {

        stmt.setString(1, adminID);
        stmt.setString(2, password);

        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Login Successful!");
            // Open the Admin Dashboard or next window
            MajorFrame AdminWindow = new MajorFrame();
            AdminWindow.setVisible(true);
            this.dispose(); // Close the login window
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Admin ID or Password!");
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
    }
   
    }//GEN-LAST:event_BtnLoginActionPerformed

